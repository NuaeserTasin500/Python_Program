## Conditions

# Any condition is boolean and printing condition shows boolean.

a = 18
print("a =", a)
print("a > 5 is", a > 5) #prints 'a > 5 is True' because 18 > 5
print("a < 5 is", a < 5) #prints 'a < 5 is False' because 18 > 5
print("a >= 5 is", a >= 5) #prints 'a >= 5 is True' because 18 > 5
print("a <= 5 is", a <= 5) #prints 'a <= 5 is True' because 18 > 5
print("a = 5 is", a == 5) #prints 'a = 5 is False' because 18 != 5 
print("a != 5 is", a != 5) #prints 'a !=5 is True' because 18 != 5
