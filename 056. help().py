## help() <S>
#In Python programming, The help() function provides interactive documentation about objects, modules, classes, functions, or keywords.


a = 10 #an integer
print(help(a)) #shows interactive documentation of integer
print()
b = 10.5 #a float
print(help(b)) #shows interactive documentation of float 


# Sometimes we can use datatype in help() function <AI>
print(help(list)) #shows interactive documentation of list
