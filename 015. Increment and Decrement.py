## increment and decrement

# increment 
x = 10
print(x) #prints 10
x = x + 1 #increment x with 1 that makes x = x + 1 = 10 + 1 = 11, so x = 11
print(x) #prints 11

# decrement
x = 10 #keep x variable's value 10 again
x = x - 1 #decrement x with 1 that makes x = x - 1 = 10 - 1 = 9, so x = 9
print(x) #prints 9

#in python, x++, ++x, x--, --x are not supported
