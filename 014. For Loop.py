## For loop

#same as for(int i=0; i<5; i++)
for i in range(5): 
    print(i)
print()

#same as for(int j=4; j<9; j++)
for j in range(4, 9): 
    print(j)
print()

#same as for(int k=4; k<20; k=k+2)
for k in range(4, 20, 2): 
    print(k)
print()


#printing a string's every letters
name = 'Dalton'
for m in name:
    print(m)
print()

#printing every element of a list
color = ['Red', 'Green', 'Blue']
for x in color:
    print(x)
