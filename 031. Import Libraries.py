## Import Libraries
# In Python, there are several libraries which can makes our work easy. For example: Math library. 


import math

result = math.sqrt(9)
print(result) #prints 3

#if you want only sqrt, then we write "from math import sqrt". 
#Then "result = math.sqrt(9)" will be "result = sqrt(9)"

# for library shortation, "import math" can be written as "import math as mt"
#Then "result = math.sqrt(9)" will be "result = mt.sqrt(9)"
