## Arithmetic operation

a = 10
b = 3

# storing operations into new variables
c = a + b #additon
d = a - b #subtraction
e = a * b #multiplication
f = a / b #division
g = a // b #floor division
h = a % b #remainder
i = a ** b #exponential (a to the power b)


# printing the operation stored variables
print("a = ", a)
print("b = ", b)
print(a, "+", b, "=", c)
print(a, "-", b, "=", d)
print(a, "*", b, "=", e)
print(a, "/", b, "=", f)
print(a, "//", b, "=", g)
print(a, "%", b, "=", h)
print(a, "**", b, "=", i)

print()


