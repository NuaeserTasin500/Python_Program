## Printing single quotation and double quotation
print('Hey, I am a \'good boy\'')
print('He said, \"You are a good person\"')

## Printing next line 
print("I am \n a good person")

## Printing backslash (\)
print("I am a good boy\\girl") 

## Printing null
print("I am a good\0boy") #prints 'I am a goodboy'

## Seperator
print("Hello", "How", "Are", "You") #default seperator is space
print("Hello", "How", "Are", "You", sep="~")
print("Hello", "How", "Are", "You", sep="#")
print('Hello', "How", "Are", "You", sep="$")
