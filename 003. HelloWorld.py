### We can write comment by using '#'
# print executes and then makes new line

print("Hello World") #string print
print(5) #integer print
print(5.9) #float print
print(3+6j) #complex number print
print(['banana', 'apple', 'orrange']) #list print
print((10, 20)) #tuple print
print(range(6)) #range print
print({'name': 'John', 'age':'35'}) #dictionary print
print({1, 2, 3, 4}) #set print
print(frozenset([1, 2, 3])) #frozenset print
print(True) #boolean print
print()

## print without new line
print('Hello ', end='')
print('World', end='')
